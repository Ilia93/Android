package com.example.workapp.presentation.screen.timer.timer;

public class TimerMenuModel {
    private String text;
    private int image;

    public TimerMenuModel(String text, int image) {
        this.text = text;
        this.image = image;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }
}
